// 12. Dado um círculo com raio r = 7, calcule sua área e circunferência. Fórmulas:
// Área: A = π * r²
// Circunferência: C = 2 * π * r
// Considere π = 3.14.

let raio = 7
let area = Math.PI * raio ** 2
let circunferencia = 2 * Math.PI * raio

console.log(`Área: ${area.toFixed(2)}`)
console.log(`Circunferência: ${circunferencia.toFixed(2)}`)